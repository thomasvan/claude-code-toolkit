#!/usr/bin/env python3
"""Routing-manifest drift check.

Verifies that every skill present in skills/INDEX.json appears in the
routing manifest output (generated by scripts/routing-manifest.py).
A skill absent from the manifest is invisible to the router.

Usage:
    python3 scripts/check-routing-drift.py
    python3 scripts/check-routing-drift.py --verbose

Exit codes:
    0 - All skills in INDEX.json appear in the routing manifest
    1 - One or more skills are absent from the routing manifest
"""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
SKILLS_INDEX = REPO_ROOT / "skills" / "INDEX.json"
MANIFEST_SCRIPT = REPO_ROOT / "scripts" / "routing-manifest.py"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--verbose", action="store_true", help="Show all skills checked")
    args = parser.parse_args()

    if not SKILLS_INDEX.exists():
        print(f"ERROR: {SKILLS_INDEX} not found", file=sys.stderr)
        return 1

    if not MANIFEST_SCRIPT.exists():
        print(f"ERROR: {MANIFEST_SCRIPT} not found", file=sys.stderr)
        return 1

    with open(SKILLS_INDEX) as f:
        idx = json.load(f)

    index_skills = sorted(idx.get("skills", {}).keys())

    # Generate manifest and check each skill appears
    result = subprocess.run(
        [sys.executable, str(MANIFEST_SCRIPT)],
        capture_output=True,
        text=True,
        cwd=str(REPO_ROOT),
    )
    if result.returncode != 0:
        print(f"ERROR: routing-manifest.py failed: {result.stderr}", file=sys.stderr)
        return 1

    manifest_text = result.stdout

    missing = [s for s in index_skills if s not in manifest_text]
    present = [s for s in index_skills if s in manifest_text]

    if args.verbose:
        print(f"Checked {len(index_skills)} skills against routing manifest")
        print(f"  Present: {len(present)}")
        print(f"  Missing: {len(missing)}")

    if missing:
        print(f"\nFAIL: {len(missing)} skill(s) in INDEX.json absent from routing manifest:")
        for skill in missing:
            print(f"  {skill}")
        print("\nEnsure each skill has valid INDEX.json entry and routing-manifest.py renders it.")
        return 1

    if args.verbose:
        print("\nPASS: routing manifest is in sync with INDEX.json")
    return 0


if __name__ == "__main__":
    sys.exit(main())
