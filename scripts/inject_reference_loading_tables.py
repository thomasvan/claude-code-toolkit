#!/usr/bin/env python3
"""Inject standardized Reference Loading Table sections into legacy components."""

from __future__ import annotations

import argparse
from dataclasses import dataclass
import importlib.util
from pathlib import Path
import re
import sys

try:
    from validate_reference_loading_tables import (
        REPO_ROOT,
        collect_component_files,
        component_name,
        has_reference_loading_table,
        reference_dir_for,
    )
except ModuleNotFoundError:
    _spec = importlib.util.spec_from_file_location(
        "validate_reference_loading_tables",
        Path(__file__).resolve().parent / "validate_reference_loading_tables.py",
    )
    assert _spec is not None and _spec.loader is not None
    _mod = importlib.util.module_from_spec(_spec)
    sys.modules["validate_reference_loading_tables"] = _mod
    _spec.loader.exec_module(_mod)  # type: ignore[attr-defined]
    REPO_ROOT = _mod.REPO_ROOT
    collect_component_files = _mod.collect_component_files
    component_name = _mod.component_name
    has_reference_loading_table = _mod.has_reference_loading_table
    reference_dir_for = _mod.reference_dir_for

TABLE_SECTION_HEADER = "## Reference Loading Table"
AUTO_MARKER = "<!-- Auto-generated by scripts/inject_reference_loading_tables.py -->"
REFERENCE_LINK_RE = re.compile(r"\[([^\]]+\.md)\]\(([^)]+\.md)\)")
HEADING_RE = re.compile(r"^(##+)\s+(.+?)\s*$", re.MULTILINE)
TABLE_SEPARATOR_RE = re.compile(r"^\|(?:\s*:?-{3,}:?\s*\|)+\s*$")
EXEMPT_COMPONENTS: set[tuple[str, str]] = {
    ("skill", "do"),
}


@dataclass
class TableRow:
    signal: str
    files: str
    why: str


def split_frontmatter(text: str) -> tuple[str, str]:
    if not text.startswith("---\n"):
        return "", text
    end = text.find("\n---\n", 4)
    if end == -1:
        return "", text
    return text[: end + 5], text[end + 5 :]


def normalize_path(path_text: str) -> str:
    cleaned = path_text.strip().strip("`")
    return cleaned.split("/")[-1]


def slug_to_words(name: str) -> list[str]:
    stem = Path(name).stem.lower()
    return [part for part in re.split(r"[-_]+", stem) if part]


def build_signal_from_filename(filename: str, owner: str) -> str:
    owner_words = set(slug_to_words(owner))
    words = [word for word in slug_to_words(filename) if word not in owner_words]
    token_map = {
        "error": "errors",
        "errors": "errors",
        "handling": "error handling",
        "testing": "tests",
        "test": "tests",
        "patterns": "implementation patterns",
        "pattern": "implementation patterns",
        "workflow": "workflow steps",
        "workflows": "workflow steps",
        "migration": "migrations",
        "migrations": "migrations",
        "performance": "performance work",
        "security": "security-sensitive work",
        "auth": "authentication",
        "authentication": "authentication",
        "navigation": "navigation",
        "state": "state management",
        "rendering": "rendering behavior",
        "async": "async behavior",
        "concurrency": "concurrency",
        "logging": "logging",
        "validation": "validation",
        "examples": "example-driven tasks",
        "checklist": "checklist-driven work",
        "criteria": "decision criteria",
        "review": "review and critique tasks",
        "comments": "comment review",
        "docs": "documentation work",
        "documentation": "documentation work",
        "api": "API design or integration",
        "ci": "CI checks",
        "cleanup": "cleanup work",
        "status": "status checks",
        "sync": "sync and push flow",
        "fix": "fixing review feedback",
        "commit": "commit preparation",
        "branch": "branch naming or branch management",
        "miner": "PR mining and pattern extraction",
        "query": "query design",
        "sql": "SQL work",
    }

    parts: list[str] = []
    for word in words:
        mapped = token_map.get(word)
        if mapped and mapped not in parts:
            parts.append(mapped)

    if not parts:
        parts = ["tasks related to this reference"]

    return ", ".join(parts[:4])


def build_why(description: str | None, filename: str) -> str:
    if description:
        return description.strip().rstrip(".")
    return f"Loads detailed guidance from `{filename}`."


def parse_existing_reference_table_candidates(text: str) -> list[TableRow]:
    rows: list[TableRow] = []
    lines = text.splitlines()
    i = 0
    while i < len(lines):
        line = lines[i].strip()
        if not line.startswith("|"):
            i += 1
            continue

        block: list[str] = []
        while i < len(lines) and lines[i].strip().startswith("|"):
            block.append(lines[i].rstrip())
            i += 1

        if len(block) < 3:
            continue

        header_cells = [cell.strip().lower() for cell in block[0].split("|") if cell.strip()]
        if not header_cells or "reference" not in " ".join(header_cells):
            continue
        if len(block) < 2 or not TABLE_SEPARATOR_RE.match(block[1].strip()):
            continue

        signal_idx = 0
        if any("trigger" in cell for cell in header_cells):
            signal_idx = next(idx for idx, cell in enumerate(header_cells) if "trigger" in cell)
        elif any("signal" in cell for cell in header_cells):
            signal_idx = next(idx for idx, cell in enumerate(header_cells) if "signal" in cell)
        elif any("task" in cell for cell in header_cells):
            signal_idx = next(idx for idx, cell in enumerate(header_cells) if "task" in cell)

        ref_idx = next(idx for idx, cell in enumerate(header_cells) if "reference" in cell)
        why_idx = None
        for idx, cell in enumerate(header_cells):
            if idx not in (signal_idx, ref_idx):
                why_idx = idx
                break

        for row in block[2:]:
            cells = [cell.strip() for cell in row.split("|") if cell.strip()]
            if len(cells) <= max(signal_idx, ref_idx):
                continue
            signal = cells[signal_idx]
            files = ", ".join(
                f"`{normalize_path(match.group(2))}`" for match in REFERENCE_LINK_RE.finditer(cells[ref_idx])
            )
            if not files:
                raw = cells[ref_idx].replace("${CLAUDE_SKILL_DIR}/references/", "").strip("`")
                files = f"`{normalize_path(raw)}`"
            why = cells[why_idx] if why_idx is not None and why_idx < len(cells) else "Routes to the matching deep reference"
            rows.append(TableRow(signal=signal, files=files, why=why.rstrip(".")))
    return rows


def parse_reference_bullets(text: str, owner: str) -> list[TableRow]:
    match = re.search(r"^##+\s+References\s*$", text, re.MULTILINE)
    if not match:
        return []

    lines = text[match.end() :].splitlines()
    rows: list[TableRow] = []
    for line in lines:
        stripped = line.strip()
        if not stripped:
            if rows:
                break
            continue
        if stripped.startswith("#"):
            break
        if not stripped.startswith("- "):
            continue

        link_match = REFERENCE_LINK_RE.search(stripped)
        if not link_match:
            continue
        filename = normalize_path(link_match.group(2))

        desc = None
        if " — " in stripped:
            desc = stripped.split(" — ", 1)[1]
        elif " - " in stripped:
            desc = stripped.split(" - ", 1)[1]

        rows.append(
            TableRow(
                signal=build_signal_from_filename(filename, owner),
                files=f"`{filename}`",
                why=build_why(desc, filename),
            )
        )
    return rows


def build_rows_for_component(component_file: Path, component_type: str) -> list[TableRow]:
    owner = component_name(component_file, component_type)
    text = component_file.read_text(encoding="utf-8")

    rows = parse_existing_reference_table_candidates(text)
    if rows:
        return rows

    rows = parse_reference_bullets(text, owner)
    if rows:
        return rows

    refs_dir = reference_dir_for(component_file, component_type)
    fallback_rows = []
    for ref_path in sorted(refs_dir.rglob("*.md")):
        filename = ref_path.name
        fallback_rows.append(
            TableRow(
                signal=build_signal_from_filename(filename, owner),
                files=f"`{filename}`",
                why=build_why(None, filename),
            )
        )
    return fallback_rows


def render_table(rows: list[TableRow]) -> str:
    body = "\n".join(f"| {row.signal} | {row.files} | {row.why} |" for row in rows)
    return (
        f"{TABLE_SECTION_HEADER}\n\n"
        f"{AUTO_MARKER}\n\n"
        "| Signal | Load These Files | Why |\n"
        "|---|---|---|\n"
        f"{body}\n"
    )


def find_insertion_offset(body: str) -> int:
    preferred = (
        "Instructions",
        "Phases",
        "Mode Detection",
        "Error Handling",
        "References",
    )
    headings = [(m.start(), m.group(2).strip()) for m in HEADING_RE.finditer(body)]
    for heading_name in preferred:
        for start, title in headings:
            if title == heading_name:
                return start
    return len(body)


def inject_table(text: str, rows: list[TableRow]) -> str:
    if has_reference_loading_table(text) or not rows:
        return text

    frontmatter, body = split_frontmatter(text)
    insert_at = find_insertion_offset(body)
    table = render_table(rows)

    before = body[:insert_at].rstrip()
    after = body[insert_at:].lstrip("\n")
    new_body = f"{before}\n\n{table}\n{after}".rstrip() + "\n"
    return frontmatter + new_body


def iter_targets(repo_root: Path, component_types: list[str]) -> list[tuple[str, Path]]:
    targets: list[tuple[str, Path]] = []
    for component_type in component_types:
        for path in collect_component_files(repo_root, component_type):
            if (component_type, component_name(path, component_type)) in EXEMPT_COMPONENTS:
                continue
            refs_dir = reference_dir_for(path, component_type)
            if refs_dir.exists() and any(refs_dir.rglob("*.md")):
                targets.append((component_type, path))
    return targets


def main() -> int:
    parser = argparse.ArgumentParser(description="Inject Reference Loading Tables into legacy components")
    parser.add_argument(
        "--component-type",
        choices=("agent", "skill"),
        action="append",
        dest="component_types",
        help="Limit injection to a component type. Repeat to include both.",
    )
    parser.add_argument("--check", action="store_true", help="Report files that would change without writing")
    args = parser.parse_args()

    component_types = args.component_types or ["agent", "skill"]
    changed = 0

    for component_type, path in iter_targets(REPO_ROOT, component_types):
        original = path.read_text(encoding="utf-8")
        updated = inject_table(original, build_rows_for_component(path, component_type))
        if updated == original:
            continue
        changed += 1
        if args.check:
            print(path.relative_to(REPO_ROOT))
            continue
        path.write_text(updated, encoding="utf-8")
        print(f"updated {path.relative_to(REPO_ROOT)}")

    if args.check:
        print(f"count: {changed}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
