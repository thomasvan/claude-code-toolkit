#!/usr/bin/env python3
"""
PreToolUse Hook: Block AI Attribution in Git Operations

Prevents "Generated with Claude Code", "Co-Authored-By: Claude", and similar
AI attribution strings from appearing in commit messages, PR bodies, and branch
names. Fires on Bash tool use only.

CLAUDE.md rules:
- No "Generated with Claude Code" attribution
- No "Co-Authored-By: Claude" lines
- Conventional commit format, focus on WHAT and WHY

Design:
- Early-exit for non-Bash tools (<1ms)
- Scans command string for forbidden patterns
- Returns exit code 2 to BLOCK the tool if found
- Sub-10ms execution
"""

import json
import re
import sys

FORBIDDEN_PATTERNS = [
    r"Generated with \[?Claude Code\]?",
    r"Co-Authored-By:.*Claude",
    r"Co-Authored-By:.*Anthropic",
    r"🤖.*Generated with",
    r"Generated by Claude",
    r"Made with Claude",
]

FORBIDDEN_RE = re.compile("|".join(FORBIDDEN_PATTERNS), re.IGNORECASE)


def main() -> None:
    try:
        data = json.loads(sys.stdin.read())
    except (json.JSONDecodeError, OSError):
        print(json.dumps({}))
        return

    tool = data.get("tool", "")
    if tool != "Bash":
        print(json.dumps({}))
        return

    command = data.get("input", {}).get("command", "")
    match = FORBIDDEN_RE.search(command)
    if match:
        # Exit code 2 = block the tool use
        result = {
            "decision": "block",
            "reason": (
                f"BLOCKED: Command contains forbidden AI attribution: '{match.group()}'. "
                "CLAUDE.md prohibits 'Generated with Claude Code' and 'Co-Authored-By: Claude' "
                "in commits and PRs. Remove the attribution and retry."
            ),
        }
        print(json.dumps(result))
        sys.exit(2)

    print(json.dumps({}))


if __name__ == "__main__":
    main()
